export interface InventoryItem {
  id: string;
  name: string;
  category: string;
  quantity: number;
  minStock: number;
  maxStock: number;
  unit: string;
  location: string;
  expiryDate?: string;
  supplier: string;
  lastRestocked: string;
  status: 'in-stock' | 'low-stock' | 'out-of-stock' | 'expiring-soon';
}

export interface Order {
  id: string;
  orderNumber: string;
  supplier: string;
  items: number;
  totalAmount: number;
  status: 'pending' | 'processing' | 'delivered' | 'cancelled';
  orderDate: string;
  expectedDelivery: string;
}

export interface ActivityLog {
  id: string;
  action: string;
  item: string;
  quantity: number;
  user: string;
  timestamp: string;
  type: 'restock' | 'dispatch' | 'adjustment' | 'order';
}

